	<li>প্রাণি হাটতে পারে না ও খুঁড়িয়ে হাটবে।</li>
	<li>চিকিৎসায় বিলম্ব হলে অধিকাংশ ক্ষেত্রে ১৮-৭২ ঘন্টার মধ্যে পশু মারা যাবে।</li>
