&nbsp;
<ul>
	<li>সাধারণত: বর্ধাকালের প্রথম দিকে এ রোগের প্রাদুর্ভাব ঘটে ।</li>
	<li>স্টাতস্যাতে জমির ঘাস খাওয়ালে এই রোগ হয়, কেননা সেখানে এ রোগের জীবাণু থাকার সম্ভাবনা থাকে।</li>
	<li>বৃষ্টি ও বন্যার পানির সাহায্যে এই রোগ ছড়ায়, তাই পানিতে ডোবা ঘাস খাওয়ালে এ রোগ হওয়ার</li>
