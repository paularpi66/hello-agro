<strong>গলাফুলা </strong><strong>রোগ (Hemorrhagic Septicemia/HS) </strong>

&nbsp;

এ রোগ ব্যাকটেরিয়া জনিত একটি মারাত্বক সংক্রামক রোগ । বাংলাদেশে প্রায় সকল খতুতে এই রোগ হয। বর্ষাকলে যখন গো/মহিষকে দিয়ে বেশি কাজ করানো হয় তখন এ রোগ বেশি হয়। বর্ষার শুরু এবং শেষে এ রোগের প্রাদুর্ভাব বেশি হয়। এ রোগ ডুবো অঞ্চলে বেশি হয় এবং মৃত্যুর হারও অনেক বেশী।

