							<strong style="font-size:18px;">&nbsp;রোগবালাই:&nbsp;</strong><br>নরম পচা রোগঃ রাইজোপাস আরটোকারপাস নামক ছত্রাকের আক্রমণে কচি ফলের (সত্রী পুস্প মঞ্জুরী) গায়ে বাদামি রংয়ের দাগের সৃষ্টি হয় এবং ছোট অবস্থাতেই ফল ঝড়ে পড়ে। পুরুষ পুস্প মঞ্জুরী পরাগায়ন শেষে স্বাভাবিকভাবেই কালো হয়ে ঝরে পড়ে।

<br>ফল পচা রোগঃ এটি ছত্রাকজনিত রোগ। এ রোগের আক্রমণে পরিপক্ক ফলের গায়ে বাদামি রংয়ের দাগ পরে এবং পরবর্তী কালে ফল পচে খাওয়ার অযোগ্য হয়ে যায়। 

<br>গামেসিসঃ এ রোগের প্রভাবে গাছের বাকলে ফাটল ধরে ও সে স্থান থেকে অবিরত রস ঝরে। কাঠ বেরিয়ে আসে, ক্ষতস্থানে গর্ত হতে থাকে ও পচন ধরে। চারা গাছ সংবেদনশীল বিধায় ধীরেধীরে মারা যায়।

<br>ফল ফেটে যায়ঃ ফলের বৃদ্ধিকালে দীর্ঘ শুষ্কতা বা পানির কমতি হলে ফলের ত্বক শক্ত হয়ে যায়। তারপর হঠাৎ অধিক বৃষ্টি বা পানি পেলে ফলের ভিতরের অংশ দ্রুত বৃদ্ধি পায়, এতে ভেতরের চাপ সহ্য করতে না পেরে ফল ফেটে যায়। জাতগত বৈশিষ্ট্যের (খাজা কাঁঠাল) জন্য নাবী মৌসুমে ফল ফাটে। এছাড়া অতি পক্কতার জন্যও ফল ফাটতে পারে। 

<br>ফলের মধ্যে বীজ অঙ্কুরোদগম হয়ঃ ফলের মধ্যে বীজের অঙ্কুরোদগম হওয়াকে ভিভিপেরি জার্মিনেশন বলে। জাতগত বৈশিষ্ট্যের কারণে, হরমোনের অভাবজনিত কারণে এবং উচ্চ আর্দ্রতা ও তাপমাত্রার কারণে ভিভিপেরি জার্মিনেশন হয়।


<br><strong style="font-size:18px;">&nbsp;দমন ব্যবস্থা:&nbsp;</strong><br>নরম পচা রোগ 
