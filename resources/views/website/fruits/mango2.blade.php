প্রতিকারঃ  আম গাছে মুকুল আসার ১০ দিনের মধ্যে একবার এবং এর একমাস পর আরো একবার প্রতি লিটার পানির সাথে ১ মি.লি. হারে সাইপারমেথ্রিন (রিপকর্ড/ সিমবুশ/ ফেনম/বাসাথ্রিন/অন্য নামের) ১০ ইসি অথবা ০.৫ মি.লি. হারে ডেল্টামেথ্রিন (ডেসিস) ২.৫ ইসি অথবা ফেনভ্যালিরেট (সুমিসাইডিন/মিলফেন/অন্য নামের) ২০ ইসি নামক কীটনাশক মিশিয়ে আম গাছের কান্ড, ডাল, পাতা এবং মুকুল ভালভাবে ভিজিয়ে স্প্রে করে হপার দমন করা সম্ভব। বর্ষা মৌসুম শেষে বছরে একবার পূর্ণ বয়স্ক আম গাছের অপ্রয়োজনীয় মৃত, অর্ধমৃত ডালপালা ছাঁটাই করে আলো বাতাস চলাচলের ব্যবস্থা করলে হপারের প্রাদুর্ভাব প্রায় ৩০-৮০ শতাংশ কমে যায়

<b><br>আমের পাতা কাটা উইভিল:</b>
প্রতিকারঃ মাটি থেকে পোকার ডিমযুক্ত নতুন কাটা পাতা সংগ্রহ করে ধ্বংস করলে এ পোকার সংখ্যা কমিয়ে ফেলা সম্ভব। নার্সারীতে নতুন বের হওয়া পাতাসহ ডগাকে মশারীর নেট দিয়ে ঢেকে দিলে পোকার আক্রমণ কম হয়। গাছে কচি পাতা বের হওয়ার সাথে সাথে কচি পাতায় প্রতি লিটার পানির সাথে ১ মি..ল. হারে সাইপারমেথ্রিন (রিপকড/র্ সিমবুস/ ফেনম/ বাসাথ্রিন) ১০ ইসি অথবা ১ মি..ল. হারে ফেনিট্রোথিয়ন (সুমিথিয়ন/ ফলিথিয়ন/ এগ্রোথিয়ন) ৫০ ইসি মিশিয়ে  স্প্রে করতে হবে। 

<b><br>ফলের মাছি পোকা: </b>
প্রতিকারঃপরিপক্ক কিন্তু সবুজ আম গাছ থেকে সংগ্রহ করলে এ পোকার আক্রমণ এড়ানো সম্ভব। পোকাক্রান্ত আমগুলো সংগ্রহপূর্বক মাটিতে গভীর গর্ত করে পূঁতে ফেলতে হবে। বাদামী কাগজ বা পলিথিন দিয়ে ফল ব্যাগিং করতে হবে। ফল সংগ্রহের অমত্মত এক মাস পূর্বে বিষটোপ ফাঁদ হিসেবে ১০০ গ্রাম পাকা আম থেঁতলে এর সাথে ১ গ্রাম সেভিন  ৮৫ ডবিস্নউপি বা সেকুফোন ৮০ এসপি বিষ ব্যবহার করতে হবে (বিষটোপ ২-৩ দিন পর পর পরিবর্তন করতে হবে) অথবা ফল সংগ্রহের এক-দেড় মাস পূর্বে মিথাইল ইউজেনলযুক্ত সেক্স ফেরোমন ফাঁদ ব্যবহার করতে হবে।

<b><br>ফল ছিদ্রকারী পোকা:</b>
প্রতিকারঃ মার্চ-এপ্রিল মাসে ঝরে যাওয়া আক্রান্ত কচি ফল মাটি থেকে সংগ্রহ করে আগুনে পুড়িয়ে ফেলতে হবে । মার্চ মাসের ১ম সপ্তাহ থেকে শুরু করে ১৫ দিন পর পর ২-৩ বার প্রতি লিটার পানির সাথে ২ মি.লি. হারে ফেনিট্রোথিয়ন(সুমিথিয়ন/অন্যনামের) ৫০ ইসি মিশিয়ে আমে স্প্রে করলে এ পোকার আক্রমণ কমানো সম্ভব হবে।

<b><br>মিলিবাগ:</b>
প্রতিকারঃ আক্রান্ত পাতা ও ডগা সংগ্রহ করে মাটিতে পুঁতে ফেলতে হবে বা পুড়িয়ে ফেলতে হবে। আক্রমণের মাত্রা বেশী হলে ডাইমেথয়েট জাতীয় কীটনাশক (পারফেকথিয়ন/টাফগর/অন্য নামের) প্রতি লিটার পানিতে ২ মি.লি. হারে মিশিয়ে ১৫ দিন পরপর ২-৩ বার আক্রান্ত গাছে ছিটাতে হবে। মিলিবাগের আক্রমণ প্রতিহত করতে চাইলে গাছের গোড়ার চারদিকে গরমের সময় খুঁড়ে দিতে হবে। এরপর ডিসেম্বর মাসে মাটি থেকে ৩০-৪৫ সে.মি. উপরে গাছের কান্ডে গ্রীজ ও আলকাতরা ১:২ অনুপাতে অথবা রেজিন ও কেস্টর অয়েল ৪:৫ অনুপাতে মিশিয়ে আঠালো বন্ধনী আকারে প্রয়োগ করতে হবে।
