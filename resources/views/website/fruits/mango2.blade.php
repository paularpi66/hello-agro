<b><br>মিলিবাগঃ</b> মে মাসের দিকে স্ত্রী পোকা গাছের গোড়ায় মাটির ৫-১৫ সে.মি. গভীরে ডিম পাড়ে। ডিসেম্বর-জানুয়ারিতে ডিম ফুটে বাচ্চা বের হয় এবং দলবদ্ধভাবে গাছের উপরের দিকে বেয়ে বেয়ে উঠতে শুরু করে। এরা কচি ডগা ও মুকূল থেকে রস চুষে খায় ফলে এগুলো শুকিয়ে যায় এবং ফলন মারাত্বকভাবে ব্যহত হয়। এছাড়া রস চোষার সাথে সাথে এরা  আঠালো মধুরস নিঃসরণ করে যা ফুল, ফল ও গাছের পাতায় আটকে যায় এবং সেখানে শুটি মোল্ড জন্মে । 

