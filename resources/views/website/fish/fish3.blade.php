<br>
বায়োফ্লক প্রযুক্তিতে উপকারী ব্যাকটেরিয়া মাছের অব্যবহৃত খাদ্য, মল-মূত্র থেকে নিঃসৃত অ্যামোনিয়াকে ব্যবহার করে অণুজীব প্রোটিনে রূপান্তর করে। এর ফলে বাড়তি খাবার খরচ কমে, পাশাপাশি খাদ্য অপচয় রোধ হয়। যেখানে প্রচলিত পদ্ধতিতে মাছ চাষে ৬০ ভাগ খরচই&nbsp; হয় খাবারের পেছনে। বায়োফ্লক পদ্ধতিতে কার্বোহাউড্রেট ও প্রোবায়োটিক সরবরাহই যথেষ্ট। প্রোটিন তৈরির কাজটি করে দেয় সিস্টেমের উপকারী ব্যাকটেরিয়া। ফলে অন্যান্য সিস্টেমের চেয়ে খাদ্য কম লাগে, বেশি লাভ করা যায়।<br>
<br>
<b>বায়োফ্লক প্রযুক্তির ইতিবাচক দিক</b><br>
<br>
সহজ চাষ পদ্ধতি: এটি একটি সহজ চাষ পদ্ধতি। সঠিক প্রশিক্ষণের মাধ্যমে কারিগরি দক্ষতা অর্জন করলে বাড়িতেই ট্যাংকে সহজে মাছ চাষ করা যায়।<br>
<br>
খুব কম পানি পরিবর্তন: মাছ চাষের অন্যতম নিয়ামক অ্যামোনিয়া নিয়ন্ত্রণের মাধ্যমে উপকারী ব্যাকটেরিয়া পানির গুণাগুণ রক্ষা করে ফলে ট্যাংকের পানি খুব কম (মোট পানির খুবই সামান্য অংশ) পরিবর্তন করলেই চলে। এমনকি সব কিছুর সঠিক মাত্রা ধরে রাখতে পারলে একবারও পানি পরিবর্তনের প্রয়োজন হয় না।<br>
<br>
জমি এবং পানির সর্বোচ্চ ব্যবহার নিশ্চিতকরণ: এই সিস্টেমে ছোট ছোট ট্যাংকে অনেক মাছ উৎপাদন করা যায়। অর্থাৎ লাইভস্টকের ঘনত্ব অনেক বেশি রাখা যায়। তাছাড়া ট্যাংকের পানি খুব কম বা একেবারেই না পরিবর্তন করলেও চলে, তাই অল্প জমি ও অল্প পানি ব্যবহার করে অধিক মাছ উৎপাদন সম্ভব হয়। যা জমি ও পানির সর্বোচ্চ ব্যবহার নিশ্চিত করে।<br>
<br>
পরিবেশবান্ধব অ্যাকোয়াকালচার সিস্টেম: প্রকৃতিতে বিদ্যমান উপকারী ব্যাকটেরিয়া ব্যবহারের মাধ্যমে মাছ চাষ করা হচ্ছে। ফলে এই পদ্ধতিতে মাছ চাষে ব্যবহৃত বিভিন্ন রাসায়নিক পদার্থ ও অ্যান্টিবায়োটিক প্রয়োজন নেই বললেই চলে। সেই সঙ্গে পানির ব্যবহার খুব কম। তাই এটি একটি পরিবেশবান্ধব মাছ চাষ পদ্ধতি।<br>
