<b>বায়োফ্লক প্রযুক্তি ব্যবহারের যেসব বিষয় খেয়াল রাখা জরুরি</b><br>
<br>
বায়োফ্লক পদ্ধতিতে উপকারী ব্যাকটেরিয়া খুবই গুরুত্বপূর্ণ ভূমিকা পালন করে। সে কারণে সঠিক উৎস হতে প্রোবায়োটিক ব্যাকটেরিয়া সংগ্রহ করতে হবে।<br>
<br>
নিয়মিত ট্যাংকে সরবরাহকৃত পানির গুণাগুণ যেমন- অ্যামোনিয়া, নাইট্রেট, নাইট্রাইট, ফ্লকের ঘনত্ব ইত্যাদি পরিমাপ করতে হবে এবং এগুলো যদি সঠিক মাত্রায় না থাকে তাহলে প্রয়োজনীয় ব্যবস্থা নিতে হবে।<br>
<br>
আমাদের দেশে দিনের বেলার তাপমাত্রা ও রাত্রের তাপমাত্রা সব সময় উঠানামা করে, যা ফ্লকের বৃদ্ধির জন্য উপযোগী নয়। তাই পর্যাপ্ত ফ্লকের বৃদ্ধির জন্য তাপমাত্রা নিয়ন্ত্রণের প্রয়োজনীয় ব্যবস্থা গ্রহণ করতে হবে।<br>
<br>
বয়োফ্লকের মাধ্যমে মাছ চাষের ক্ষেত্রে অবশ্যই প্রশিক্ষণপ্রাপ্ত দক্ষ জনবলের প্রয়োজন। কারণ এই প্রযুক্তিতে যদি পানির গুণাগুণ পরীক্ষা, সঠিক মাত্রায় খাদ্য প্রয়োগ, ফ্লকের ঘনত্ব পরিমাপ ইত্যাদি বিষয়ে কারিগরি জ্ঞান না থাকে তাহলে চাষি যে কোন সময় ক্ষতিগ্রস্ত হতে পারে।<br>
<br>
বায়োফ্লক পদ্ধতিতে ট্যাংকে অধিক পরিমাণে মাছ রাখা হয়। তাই ট্যাংকে অক্সিজেন সরবরাহ করতে হয়। আর ট্যাংকে সব সময় অক্সিজেন সরবরাহ করার জন্য সার্বক্ষণিক বিদ্যুৎ লাগবে। তা না হলে ট্যাংকের অক্সিজেন সরবরাহ বন্ধ হয়ে সব মাছ এক সাথে মারা যেতে পারে। সর্বোচ্চ এক ঘণ্টা ট্যাংকে অক্সিজেন সরবরাহ না করা হলে সব মাছ মারা যেতে পারে। এক্ষেত্রে খরচ কমাতে চাইলে ছোট খামারের জন্য সোলার পাওয়ার ব্যবহার করা যেতে পারে। তবে খামারের আকার যদি বড় হয় তাহলে সোলার প্যানেল থেকে যে বিদ্যুৎ পাওয়া যাবে তা দিয়ে প্রয়োজনীয় ক্ষমতার মোটর চালানো সম্ভব হবে না। সে ক্ষেত্রে মূল গ্রিড লাইনের বিদ্যুৎ নেওয়াই ভালো।<br>
