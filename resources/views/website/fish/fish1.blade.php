বাণিজ্যিকভাবে খাঁচায় মাছ চাষ পরিচালনার জন্য প্রবাহমান পানিতে ভাসমান খাদ্যের বিকল্প নেই। মাছ খাঁচায় মজুদের পর হতে বাজারজাত করার পূর্ব পর্যন্ত দৈহিক ওজনের বিবেচনায় খাদ্য প্রয়োগের মাত্রা ৮ শতাংশ হতে ৩ শতাংশের মধ্যে সীমিত রাখতে হবে। মাছের ওজন ৩০০-৫০০ গ্রাম হওয়া পর্যন্ত সম্পূরক খাদ্য প্রদান অব্যাহত রাখতে হবে। বর্তমানে কারখানায় তৈরি ভাসমান খাবার ব্যবহার করে দেখা গেছে, মজুদ থেকে শুরু করে বাজারজাত পর্যন্ত ৭৫০-১০০০ গ্রাম ওজনের মাছ উৎপাদন করতে সর্বোচ্চ ১.৫ কেজি খাদ্যের প্রয়োজন হয়।<br>
<br>
<b>খাঁচায় মাছ গ্রেডিং বা বাছাইকরণ</b><br>
সাধারণত প্রত্যাশিত উৎপাদনের জন্য খাঁচায় পোনা মজুদের তিন সপ্তাহ পর প্রথম বার খাঁচার মাছ বাছাই করতে হবে (তবে জাত ভেদে ভিন্ন সময় হতে পারে) । দিনের তাপমাত্রার দিকে লক্ষ্য রেখে সকাল বেলা কিংবা পড়ন্ত বিকেলে খাঁচার মাছ বাছাই করা উচিত। যখন নদীর পানি বেশি প্রবাহমান থাকে তখন খাঁচার পানি দ্রুত পরিবর্তিত হতে থাকে। এ সময় খাঁচার মাছ বাছাই করা অধিক উপযোগী। বাজারজাত করার আগে প্রয়োজন অনুসারে দুই তিনবার বাছাই করতে হবে।<br>
