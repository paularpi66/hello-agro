পৃথিবীর বিভিন্ন দেশে খাঁচায় মাছ চাষ হচ্ছে। চীনে এ প্রযুক্তি বেশ জনপ্রিয়। যতদূর জানা যায়- চীনেই প্রথম ছোট খাঁচায় বেশি ঘনত্বে মাছ চাষ শুরু হয়। চীনা খামারিদের মধ্যে সম্পূরক খাবার দিয়ে খাঁচায় তেলাপিয়া এবং কমন কার্প চাষ খুব জনপ্রিয়। তাইওয়ানেও খাঁচায় মাছ চাষ করা হয়। সেখানে ৫০ থেকে ১০০ গ্রাম ওজনের তেলাপিয়া খাঁচায় চাষ করে ৫০০ থেকে ৬০০ গ্রামের তেলাপিয়ায় পরিণত করা হয় এবং পরে ফিলেট আকারে রফতানি করা হয়। দেশটি ২০০৬ সালেই ২০ হাজার টন হিমায়িত তেলাপিয়া এবং ৩ হাজার ১০০ টন তেলাপিয়ার ফিলেট রফতানি করে।<br>
<br>
ইন্দোনেশিয়ায়ও খাঁচায় মাছ চাষ জনপ্রিয়, তবে জাভা অঞ্চলে এই প্রযুক্তির প্রচলন অনেক বেশি। জাভা অঞ্চলের ‘সিরাতা’য় এক সময় প্রায় ৩০ হাজার খাঁচায় মাছ চাষ করা হতো<br>
<br>
থাইল্যান্ডের দক্ষিণাঞ্চলে অতি সাধারণ জাল দিয়ে তৈরি খাঁচায় এক বছরের জন্য কোরাল অথবা ভেটকি মাছ চাষ করা হয়। সমুদ্রে ছোট মাছ ধরে তা খাঁচায় মাছের জন্য খাবার হিসেবে দেওয়া হয়। আর ভিয়েতনামে স্রোতহীন নদীতে খাঁচায় জলজ আগাছা খাদ্য হিসেবে দিয়ে গ্রাসকার্প এবং তেলাপিয়া মাছ চাষ করা হচ্ছে<br>
<br>
বাংলাদেশে বিভিন্ন সময়ে খাঁচায় মাছ চাষের উদ্যোগ নেওয়া হলেও চাঁদপুর জেলার ডাকাতিয়া নদীতে থাইল্যান্ডের প্রযুক্তি অনুকরণে খাঁচায় মাছ চাষ প্রথম সবার দৃষ্টি আকর্ষণ করে। এ উদ্যোগের অগ্রপথিক কুমিল্লার শিল্প নগরী এলাকায় (বিসিক) অবস্থিত জাল ও সুতা ফ্যাক্টরি “ফরিদ ফাইবার অ্যান্ড উইভিং লিমিটেড” এর স্বত্বাধিকারী মো. বেল্লাল হোসেন। ২০০২ সাল থেকে শুরু করে মাত্র ছয় বছরের ব্যবধানে বর্তমানে চাঁদপুরের ডাকাতিয়া নদী ও লক্ষ্মীপুর জেলায় মেঘনা নদীর রহমতখালী চ্যানেলে খাঁচায় মনোসেক্স তেলাপিয়া করা হচ্ছে। বাংলাদেশে বাণিজ্যিকভাবে সফলতার সাথে খাঁচায় মাছ চাষের অধ্যায় শুরু হয় চাঁদপুর জেলার ডাকাতিয়া নদীতে। এজন্য এখানকার ব্যবস্থাপনা ও উৎপাদন মডেলকে “ডাকাতিয়া মডেল” বলা হয়। সিলেট অঞ্চলের ৬টি এলাকা দিরাইয়ে সুরমা নদীতে খাঁচায় মাছ চাষ করে দারিদ্র্যমুক্ত হয়েছে।<br>
<br>
<b>খাঁচায় মাছ চাষের সুবিধা</b><br>
১. ভাসমান খাঁচায় মাছ চাষ করলে পুকুরের মতো জলাশয়ের প্রয়োজন হয় না<br>
২. প্রবাহমান নদীর পানিকে যথাযথ ব্যবহার করে মাছ উৎপাদন বাড়ানো যায়<br>
৩. মাছের বর্জ্য প্রবাহমান পানির সাথে অপসারিত হয়, ফলে পানিকে দূষিত করতে পারে না<br>
৪. মাছের উচ্ছিষ্ট খাদ্য খেয়ে নদীর প্রাকৃতিক মৎস্য প্রজাতির প্রাচুর্য্য বৃদ্ধি পায়<br>
৫. প্রবাহমান থাকায় প্রতিনিয়ত খাঁচার পানি পরিবর্তন হতে থাকে। এ কারণে পুকুরের চেয়ে অধিক ঘনত্বে মাছ চাষ করা যায়<br>
৬. উন্মুক্ত প্রবাহমান পানিতে থাকার কারণে মাছের রোগবালাই হয় না বললেই চলে<br>
<br>
<b>খাঁচায় যেসব মাছ চাষ করা যায়</b><br>
খাঁচায় প্রায় সব ধরনের মাছই চাষ করা সম্ভব। তবে এর মধ্যে মনোসেক্স তেলাপিয়া, মাগুর, শিং, পাঙ্গাস এসব মাছের উৎপাদন ভালো।<br>
<br>
<b>খাঁচা স্থাপনের উপযুক্ত স্থান</b><br>
<b>ক. </b>খাঁচা স্থাপনের জন্য নদী বা জলাশয়ের এমন অংশ নির্বাচন করতে হবে যেখানে একমুখি প্রবাহ বা জোয়ার ভাটার শান্ত প্রবাহ রয়েছে। নদীর মূল প্রবাহ যেখানে স্রোত অনেক বেশি সেখানে খাঁচা স্থাপন না করাই ভালো। স্রোতের গতি প্রতি সেকেন্ডে ৪-৮ ইঞ্চি বা সর্বোচ্চ সেকেন্ডে ১৬ ইঞ্চি এমন স্থানে খাঁচা স্থাপন করতে হবে।<br>
<br>
<b>খ.</b> মূল খাঁচা পানিতে ঝুলন্ত রাখার জন্য কমপক্ষে ১০ ফুট গভীরতা থাকা দরকার। যদিও প্রবাহমান পানিতে তলদেশে বর্জ্য জমে সৃষ্ট গ্যাসের কারণে খাঁচার মাছের ক্ষতি হওয়ার সম্ভবনা কম, তারপরও নদীর তলদেশের কাদা থেকে কমপক্ষে ৩ ফুট উপরে খাঁচা স্থাপন করা উত্তম।<br>
<br>
<b>গ.</b> স্থানটি লোকালয়ের কাছে হলে যদি নিরাপত্তা নিশ্চিত&nbsp; তাহলে সেটিই করতে হবে। তা না হলে অস্থায়ী ঘর তুলে পাহারার ব্যবস্থা করতে হবে।<br>
<br>
<b>ঘ.</b> খাঁচা স্থাপনের স্থান থেকে যোগাযোগ ব্যবস্থা ভালো হতে হবে যাতে মাছ বাজারজাত করা সহজ হয়।<br>
<br>
<b>ঙ.</b> খাঁচা স্থাপনের কারণে যাতে কোনোভাবেই নৌ চলাচল বিঘ্নিত না হয় সেদিকে খেয়াল রাখতে হবে।<br>
<br>
<b>চ.</b> সবচেয়ে গুরুত্বপূর্ণ বিষয় হলো, শিল্প বা কারখানার বর্জ্য কিংবা পয়ঃনিষ্কাশনের পানি, কৃষিজমির কীটনাশক মিশ্রিত পানি এসে নদীতে পড়ে এমন স্থানে খাঁচা রাখা যাবে না। তাতে মাছ মারা যেতে পারে।<br>
<br>
<b>ভাসমান খাঁচা তৈরির উপকরণ</b><br>
খাঁচা তৈরির সব উপকরণই এখন দেশে পাওয়া যায়। এর একটি তালকা দেওয়া হলো:<br>
পলিইথিলিন জাল (৩/৪ ইঞ্চি থেকে ১ ইঞ্চি ম্যাশের)<br>
রাসেল নেট (খাদ্য আটকানোর বেড়া তৈরিতে)<br>
নাইলনের দড়ি ও কাছি<br>
কভার নেট বা ঢাকনা জাল (পাখির উপদ্রব থেকে রক্ষার জন্য)<br>
১ ইঞ্চি জিআই পাইপ (৭০ ফুট প্রতিটি খাঁচার জন্য)<br>
ফ্রেম ভাসমান রাখার জন্য খালি ব্যারেল/ড্রাম (২০০ লিটারের পিভিসি ড্রাম, ওজন ৯ কেজির বেশি)<br>
খাঁচা স্থির রাখার জন্য গেরাপি (অ্যাঙ্কর)<br>
ফ্রেমের সাথে বাঁধার জন্য মাঝারি আকারের সোজা বাঁশ (প্রয়োজনীয় সংখ্যক)<br>
<br>
<b>খাঁচার আকার</b><br>
খাঁচা তৈরির জন্য এমন জাল ব্যবহার করতে হবে যেন কাঁকড়া, গুঁইসাপ, জলঢোঁড়া, কচ্ছপ ইত্যাদি ক্ষতিকর জলজ প্রাণী ফাঁস ভেদ করে বা জাল কেটে ঢুকতে না পারে। ডাকাতিয়া মডেলের খাঁচার জন্য সাধারণত দুই আকারের জাল তৈরি করা হয়, যেমন: ২০ ফুটx ১০ ফুটx ৬ ফুট এবং ১০ ফুটx ১০ ফুটx ৬ ফুট। খাঁচা তৈরির জন্য জালগুলোর ম্যাশ ৩/৪ ইঞ্চি থেকে ১১/৪ ইঞ্চির মধ্যে হওয়া ভালো।&nbsp; এতে সহজে নদীর পরিষ্কার পানি প্রতিনিয়ত খাঁচার ভেতর দিয়ে সঞ্চালিত হতে পারে।<br>
<br>
<b>ফ্রেম তৈরি ও স্থাপন</b><br>
খাঁচাগুলোর ফ্রেম তৈরি করতে প্রথমত ১ ইঞ্চি জিআই পাইপ দিয়ে আয়তাকার ২০ ফুটx ১০ ফুট ফ্রেম তৈরি করা হয়। আর মাঝে ১০ ফুট আরেকটি পাইপ বসিয়ে ঝালাই করে ফ্রেম তৈরি করা হয়। এতে একটি ফ্রেমে সরাসরি ২০ ফুটx ১০ ফুট আকারের খাঁচা বসানো যায়। আবার প্রয়োজনে ১০ ফুটx ১০ ফুট আকারের দু’টি খাঁচাও বসানো যায়। প্রতি দুই ফ্রেমের মাঝে ৩টি ড্রাম স্থাপন করে সারিবদ্ধভাবে ফ্রেমগুলো স্থাপন করা হয়। প্রয়োজনীয় সংখ্যক গেরাপি বা নোঙর দিয়ে খাঁচা নদীর নির্দিষ্ট স্থানে শক্তভাবে বসানো হয়। এরপর প্রতিটি ফ্রেমের সাথে পৃথক পৃথক জাল সেট করা হয়।<br>
<br>
<b>খাঁচায় মাছের ঘনত্ব&nbsp;</b><br>
পানির স্রোত, জালের ফাঁসের আকার, পানির গভীরতা, প্রত্যাশিত আকারের মাছ উৎপাদন, খাদ্যের গুণগতমান এবং বিনিয়োগ ক্ষমতা ইত্যাদি বিবেচনা করেই মজুদ ঘনত্ব নির্ধারণ করা হয়। স্থাপিত খাঁচায় মাছের প্রজাতি ভেদে যেমন প্রতি ঘনমিটারে ৩০ থেকে ৪০টি পর্যন্ত মনোসেক্স তেলাপিয়া পোনা মজুদ করা যাবে। মজুদকালে পোনার আকার এমন হতে হবে যাতে জালের মেসের মধ্য দিয়ে বেরিয়ে যেতে না পারে। কমপক্ষে ২৫-৩০ গ্রাম আকারের পোনা মজুদ করতে হবে।&nbsp;<br>
