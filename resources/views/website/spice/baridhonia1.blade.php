<br>ধনিয়ার একটি ক্ষতিকারক রোগ। Protomyces macrosporum ছত্রাকের আক্রমণে কান্ড ফোলা রোগ হয়ে থাকে এবং অনুকুল আবহাওয়ায় ক্ষতির পরিমাণ প্রায় ৮০ ভাগ। শিকড় ব্যতিত গাছের সকল অংশ যেমন- কান্ড, শাখা-প্রশাখা, পাতা, ফুল ও ফল এ জীবাণু দ্বারা আক্রান্ত হয়ে থাকে। এ রোগের আক্রমণে ধনিয়ার কান্ড বা শাখা প্রশাখা ফুলে যায়। ধীরে ধীরে এ ফোলা বিস্তৃত হয়ে সমম্ত কান্ড, শাখা-প্রশাখা ও পুষ্প মঞ্জুরী আক্রান্ত হয়। পুষ্প মঞ্জুরীতে আক্রমণ হলে ফুল শুকিয়ে মরে যায়।এটি একটি মাটি বাহিত এবং বীজ বাহিত রোগ যা বীজের সাথে বা বাতাসে ধূলা বালির সাথে উড়ে এক জমি হতে অন্য জমিতে বা এক এলাকা হতে অন্য এলাকায় বিস্তার লাভ করে থাকে। 								</div>  
