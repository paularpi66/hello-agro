		<b>বারবারি ছাগল </b>

বারবারি জাতের ছাগল মাঝারি আকারের। তবে দুনিয়াজুড়ে এরা নান্দনিক সৌন্দর্য্য, অধিক উৎপাদনশীলতা, সুস্বাদু মাংস ও দ্রুত বৃদ্ধির জন্য বিখ্যাত। মুখটা চিকন ও হরিণের মতো মায়াবী, পা দুটোও হরিণের মতো।

বারবারি ছাগলের অতি দ্রুত বৃদ্ধি ঘটে এবং এক বছর (১২ মাস) থেকে ১৪ মাসের মধ্যে এই ছাগল দুইবার বাচ্চা প্রসব করে। এ জাতের ছাগী প্রতিবারে দুই থেকে তিনটি বাচ্চা জন্ম দেয়।

এই জাতের ছাগল মূলত ভারতের হরিয়ানা, পাঞ্জাব ও উত্তরপ্রদেশ এবং পাকিস্তানের পাঞ্জাব ও সিন্ধুতে পাওয়া যায়।

খামার করার জন্য বারবারি ছাগল অত্যন্ত উপযোগী। এই জাতের ছাগলের আবহাওয়ার সাথে খাপ খাওয়ানোর সক্ষমতা অনেক বেশি। এরা শুকনো খাবার খেয়ে বা দেশি ছাগলের মতোই মাঠে চরে খেয়ে বেঁচে থাকতে পারে।

বারবারি ছাগলের মাংস অত্যন্ত সুস্বাদু। ভারতে এই ছাগল প্রচুর পরিমাণে পাওয়া যায়।

<b>বৈশিষ্ট্য </b>

বারবারি ছাগলের আকার মাঝারি ধরনের। এরা দেখতে খুবই আকর্ষণীয় এবং সদা সতর্ক অবস্থায় থকে। এদের কান খাড়া এবং পাঁঠার মুখে ঘন দাঁড়ি থাকে। পাঁঠা ও পাঁঠির মোড়ানো শিং থাকে যেটা উপরে বা পেছনে খাড়া থাকে এবং দৈর্ঘ্যে মধ্যম আকারের হয়ে থাকে।
