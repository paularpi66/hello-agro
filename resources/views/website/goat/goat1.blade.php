		<b>ব্ল্যাক বেঙ্গল ছাগল</b>

এটিই বাংলাদেশের একমাত্র জাত। বাংলাদেশ ছাড়াও ভারতের পশ্চিমবঙ্গ ও আসামে এ জাতের ছাগলের দেখা যায় ।

<b>সাধারণত বৈশিষ্ট্য</b>

গায়ের রঙ কালো, তবে সাদা, সাদা-কালো, খয়েরি-কালো, খয়েরি ইত্যাদি হতে পারে । শরীরের আকার ছোট। গায়ের লোম মসৃণ ও ছোট । এদের কান ও শিং ছোট এবং ছাগীর তুলনায় পাঁঠার শিং তুলনামূলক বড় ।

<b>দুধ উৎপাদন</b>

সাধারণত এ জাতের ছাগী দৈনিক ২০০-৩০০ মিলি লিটার দুধ দেয়। তবে উপযুক্ত খাদ্য ও উন্নত ব্যবস্থাপনায় ছাগী দৈনিক ১ লিটার পর্যন্ত দুধ দিয়ে থাকে। এদের দুগ্ধ প্রদান কাল সাধারণত ২-৩ মাস।

