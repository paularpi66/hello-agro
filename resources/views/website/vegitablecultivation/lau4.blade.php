									<br><strong style="font-size:18px;">&nbsp;দমন ব্যবস্থা:&nbsp;</strong><br><b>ফলের মাছি পোকা:</b>
<br>দুই ধাপে এ পোকা কার্যকারভাবে দমন করা যায়।
<br>১। পরিষ্কার পরিচ্ছন্ন চাষাবাদঃ মাছি পোকায় আক্রান্ত ফল দ্রুত পচে যায় এবং গাছ হতে মাটিতে ঝরে পড়ে। এই ফল কোন ক্রমেই জমির আশেপাশে ফেলে রাখা উচিত নয়। কারণ উক্ত ফলে লুকিয়ে থাকো পরিপূর্ণ কীড়া অল্প সময়ের মধ্যেই পুত্তুলি ও পরবর্তীতে পূর্ণাঙ্গ পোকায় পরিণত হয়ে নতুন ভাবে আক্রমণ শুরু করতে পারে। আক্রান্ত ফলসমূহ সংগ্রহ করে (যেহেতু এ পোকার কীড়া সর্মহ মাটির ১০-১২ সেমি গভীরে পুত্তলিতে পরিণত  হয়) কমপক্ষে ৩০ সেমি পরিমাণ গর্ত করে মাটিতে পুতে ফেলতে হবে অথবা হাত বা পা দিয়ে পিষে মেরে ফেলতে হবে।
<br>২। সেক্স ফেরোমন ও বিষটোপ ফাঁদের যৌথ ব্যবহারঃ কিউওির নামক সেক্স ফেরোমন পানি ফাঁদের মাধ্যমে ব্যবহার করে প্রচুর পরিমাণে পুরুষ মাছি পোকা আকৃষ্ট করে মাছি পোকা সর্মহকে মেরে ফেলা যায়। ফাঁদ প্রতি এক মিলি পরিমাণ সেক্স ফেরোমন একখন্ড তুলার টুকরায় ভিজিয়ে পানি ফাঁদের প্লাষ্টিক পাত্রের মুখ হতে ৩-৪ সেমি নীচে একটি সরু তার দিয়ে স্থাপন করতে হবে। ফেরোমনের গন্ধে আকৃষ্ট হয়ে পুরুষ মাছি পোকা প্লাষ্টিক পাত্রের অভ্যন্তরে প্রবেশ করে ও সাবান পানিতে পড়ে আটকে মারা পড়ে। বিষটোপ ফাঁদে পূর্ণবয়স্ক স্ত্রী ও পুরুষ মাছি পোকা আকৃষ্ট হয় এবং ফাঁদে পড়ে মারা যায়। একশত গ্রাম পাকা মিষ্টি কুমড়া কুচি কুচি করে ক্টে তা থেতলিয়ে ০.২৫ গ্রাম মিপসিন ৭৫ পাউডার অথবা সেভিন ৮৫ পাউডার এবং ১০০ মিলিলিটার পানি মিশিয়ে ছোট একটি মাটির পাত্রে নিয়ে তিনটি খুঁটির সাহায্যে এমনভাবে স্থাপন করতে হবে যাতে বিষটোপের পাত্রটি মাটি থেকে ০.৫ মিটার উঁচুতে থাকে। বিষটোপ তৈরির পর ৪-৫ দিন পর্যন্ত ব্যবহার করে তা ফেলে দিয়ে আবার নতুন করে তৈরি বিষটোপ ব্যবহার করতে হয়। সেক্স ফেরোমন ও বিষটোপ ফাঁদ কুমড়া জাতীয় ফসলের জমিতে ১২ মি দুরে দূরে স্থাপন করতে হবে।

<br><b>পামকিন বিটল:</b>
<br>১। চারা আক্রান্ত হলে হাত দিয়ে পূর্ণবয়স্ক পোকা ধরে মেরে ফেলা।
<br>২। ক্ষেত সব সময় পরিষ্কার রাখা।
<br>৩। চারা অবস্থায় ২০-২৫ দিন পর্যন্ত মশারির জাল দিয়ে চারাগুলো ঢেকে রাখলে এ পোকার আক্রমণ থেকে গাছ বেঁচে যায়।
<br>৪। আক্রমণের হার বেশী হলে চারা গাজানোর পর প্রতি মাদার চারদিকে মাটির সাথে চারা প্রতি ২-৫ গ্রাম অনুমোদিত দানাদার কীটনাশক (কার্বোফুরান জাতীয় কীটনাশক) মিশিয়ে গোড়ায় পানি সেচ দেয়া।
