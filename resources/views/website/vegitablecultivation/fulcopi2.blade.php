<br>২। আক্রান্ত পাতার পোকা ২-৩ বার হাতে ধরে মেরে ফেললে এই পোকা অনেকাংশে দমন করা সম্ভবপর হয়।
<br>৩। পিঁপড়া এবং মাকড়সা এ পোকার কীড়া খায়। বহু রকমের বোলতা যেমন-ট্রাইকোগ্রামা, কোটেসিয়া ইত্যাদি এই পোকার ডিম ও কীড়াকে ধ্বংস করে ফেলে। তাই এদের সংরক্ষণ করা ।
<br>৪। ব্যাসিলাস খুরিনজিয়েনসিস (বিটি) প্রয়োগ করে এ পোকা নিয়ন্ত্রণ করা যায়।
জমি জরিপ করে যদি প্রয়োজন হয় তখন সঠিক নিয়মে অনুমোদিত কীটনাশক প্রয়োগ করা।

<br><b>জাব পোকা:</b>
<br>১। প্রাথমিক অবস্থায় আক্রান্ত পাতা ও ডগার জাব পোকা হাত দিয়ে পিষে মেরে ফেলা যায়।
<br>২। নিম বীজের দ্রমন (১ কেজি পরিমান অর্ধভাঙ্গা নিম বীজ ১০ লিটার পানিতে ১২ ঘন্টা ভিজিয়ে রাখতে হবে) বা সাবান গুলা পানি (প্রতি ১০ লিটার পানিতে ২ চা চামচ গুড়া সাবান মেশাতে হবে) স্প্রে করেও এ পোকার আক্রমণ অনেকাংশে কমানো যায়।
<br>৩।  লেডীবার্ড বিটলের পূর্ণাঙ্গ পোকা ও কীড়া এবং সিরফিড্ ফ্লাই এর কীড়া জাব পোকা খেয়ে প্রাকৃতিকভাবে দমন করে। সুতরাং উপরোক্ত বন্ধু পোকাসমূহ সংরক্ষণ করলে এ পোকার আক্রমণ অনেকাংশে কম হয়।
<br>৪। আক্রমণের মাত্রা বেশী হলে শুধুমাত্র আক্রান্ত স্থানসমূহে কীটনাশক প্রয়োগ করা যেতে পারে। এ ক্ষেত্রে স্বল্প মেয়াদী বিষক্রিয়া সম্পন্ন কীটনাশক, যেমন-ম্যালাথিয়ন ৫৭ ইসি প্রতি লিটার পানিতে ২ মিলি হারে অথবা পিরিমর ৫০ ডিপি প্রতি লিটার পানিতে ১ গ্রাম হারে মিশিয়ে স্প্রে করতে হবে। পিরিমর মৌমাছিও পরাগায়নে সাহায্যকারী পোকাদের জন্য অনেকটা নিরাপদ।
<br><b>কাটুই পোকা :</b>
<br>১। ভোর বেলা কর্তিত চারার গোড়া খুঁড়ে কীড়াগুলো মেরে ফেলা।
<br>২। টর্চ বা হারিকেন নিয়ে রাতের বেলা কাটুই পোকার কীড়া সংগ্রহ করে মেরে ফেলা।
<br>৩। ক্ষেতে সেচ দিলে মাটির নিচে লুকিয়ে থাকা কীড়া মাটির উপরে উঠে আসে। ফলে সহজে পাখি এদের ধরে খায় এবং হাত দিয়ে মেরে ফেলা যায়।
<br>৪। বিষটোপ দিয়ে এ পোকা দমন করা যায়। বিষটোপ হিসেবে শতাংশ প্রতি ২ গ্রাম  সেভিন/কার্বারিল ৮৫ ডবি­উ পি অথবা পাদান ৫০ এসপি, ৪০০ গ্রাম গম বা ধানের কুঁড়ার সাথে পরিমাণমত পানিতে মিশিয়ে এমন একটি বিষটোপ তৈরি করতে হবে যা হাত দিয়ে ছিটানো যায়। এ বিষটোপ সন্ধ্যাবেলা আক্রান্ত ক্ষেতে চারাগাছের গোড়ায় ছিটিয়ে প্রয়োগ করলে কাটুই পোকার কীড়া দমন সহজ হয়।
