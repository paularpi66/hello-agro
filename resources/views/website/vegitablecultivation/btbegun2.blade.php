<br>পূর্ণ বয়স্ক পোকা ও নিম্ফ উভয়েই গাছের নতুন ডগা ও পাতা থেকে রস চুষে খায়। এদের আক্রমণে পাতা কুঁকড়ে যায়, হলদে রং ধারণ করে, গাছের বৃদ্ধি কমে যায়।
<br>দমন ব্যবস্থাপনা:
<br>• সংগ্রহের পর ফসলের অবশিষ্টাংশ নষ্ট করতে হবে।
<br>• আঠালো হলুদ ফাঁদ ব্যবহার করতে হবে।
<br>• বায়োনিম প্লাস (Azadiractin)@ ১ মিলি/ লিটার পানিতে মিশিয়ে আক্রান্ত পাতায় স্প্রে করতে হবে।
<br>• সুমিথিয়ন ৫০ ইসি বা ম্যালাথিয়ন ৫৭ ইসি বা ফাসটাক ২ ইসি প্রতি লিটার পানিতে ১ মিলি অথবা এসাটাফ ৭৫ ডব্লিউপি প্রতি লিটার পানিতে ১ গ্রাম বা এডমায়ার ২০০ এসএল প্রতি লিটার পানিতে ০.৫ মিলি মিশিয়ে পাতায় স্প্রে করতে হবে। 

<br><b>পাতার হপার পোকা :</b>
<br>আক্রমণের ধরণ:
<br>পূর্ণাঙ্গ ও অপূর্ণাঙ্গ উভয় অবস্থাতেই জ্যাসিড পাতার রস চুষে খায়। পাতায় হলদে বা সাদাটে দাগ পড়ে, কচি পাতা কুঁচকে যায় এবং শেষে শুকিয়ে ঝরে পড়ে। চারা গাছে আক্রমণ হলে চারার বৃদ্ধি ব্যাহত হয়, গাছ দূর্বল হয় এবং পরবর্তীতে ফলন বেশ কম হয়। অতিরিক্ত আক্রমণে পাতার ফ্লোয়েম টিউব নষ্ট হয়ে যায়, পাতা ঝলসে যায়।
<br>দমন ব্যবস্থাপনা:
<br>• ফসলের অবশিষ্টাংশ নষ্ট করা এবং আগাছা পরিষ্কার। 
<br>• ডিটারজেন্ট মিশ্রিত পানি (৫ গ্রাম/লিটার) পাতার নিচের দিকে
স্প্রে করতে হবে।
<br>• আক্রমণের শুরুতে বায়োনিম প্লাস (Azadiractin) অথবা Polo 500 SC (Difenthuron) অথবা ইনটিপ্রিড ১০ এস সি (Chlorfenapyr) প্রতি লিটার পানিতে  ১ মিলি মিশিয়ে পাতায় স্প্রে করতে হবে। 

<br><b> লাল মাকড় :</b>
<br>আক্রমণের ধরণ:
<br>এরা কোষ ছিদ্র করে  পাতা থেকে রস চুষে খায়। ফলে পাতার উপরের অংশে ফ্যাকাশে রং ধারণ করে এবং গাছের খাদ্য তৈরী প্রক্রিয়া বাধাগ্রস্থ হয়। গাছের বৃদ্ধি ও ফুল ফল ধারণ বিঘ্নিত হয় এবং ফলন কমে যায়। অতিরিক্ত আক্রমণে পাতার নিচের অংশ বাদামী রং ধারণ করে, পরবর্তীতে পুড়ে যাওয়ার মতো দেখায়।
<br>দমন ব্যবস্থাপনা:
<br>• জমি, জমির আইল, সেচ নালা আগাছা মুক্ত রাখতে হবে। 
<br>• আক্রান্ত ফসলে উপরি সেচ প্রয়োগ করতে হবে। ধুলাবালি থাকলে এদের আক্রমণ বেড়ে যায়। ভারী বৃষ্টিপাতে মাইটের আক্রমণ কমে যায়। 
<br>• মাকড় নাশক  Abamectin (ভার্টিমেক ১.৮ ইসি  অথবা  Abom ১.৮ ইসি  অথবা Ambush ১.৮ ইসি) প্রতি লিটার পানিতে ২.৫ মিলি  অথবা Propargite (Sumite or Omite ৫৭ ইসি) প্রতি লিটার পানিতে ২ মিলি মিশিয়ে স্প্রে করতে হবে।

