									বার্লিতে তেমন কোন পোকা-মাকড়ের আক্রমণ দেখা যায় না । তবে মাঝে মধ্যে চারা অবস্থায় বার্লির ক্ষেতে “তার পোকা” দেখা যায়। গাছের বয়স যখন ২৫-৩৫ দিন হয় তখন তার পোকার আক্রমন পরিলক্ষিত হয় ফলে Dead heart লক্ষণ দেখা দেয়। এ ধরণের আক্রান্ত গাছ উঠালে দেখা যাবে গাছের গোড়ার অংশ থেতলিয়ে গেছে। এছাড়াও চারা অবস্থায় কাটুই পোকার উপদ্রব দেখা দিতে পারে। <br><strong style="font-size:18px;">&nbsp;দমন ব্যবস্থা:&nbsp;</strong>তার পোকা আক্রান্ত ডগা সংগ্রহ করে কীড়াসহ ধ্বংস করতে হবে। তাছাড়া যে সমস্ত এলাকায় এ পোকার আক্রমণ বেশী হয় সেখানে হেক্টর প্রতি ১৮ কেজি কার্বোফুরান (ফুরাডান ৫ জি) বীজ বপনের সময় মাটিতে প্রয়োগ করতে হবে। সাধারণত বেলে ও বেলে দোআঁশ জমিতে তার পোকার আক্রমণ  বেশি হয়।  সময় ও স্থান ভেদে এ পোকার দ্বারা ৭.৫% পর্যন্ত চারা গাছ আক্রান্ত হতে পারে। ফসল তোলার পর আক্রান্ত জমি ভালভাবে চাষ করলে এ পোকার বংশ বৃদ্ধি কমে যায়। 
<br>কাটুই পোকা দমনের জন্য দিনের বেলা আক্রান্ত গাছের গোড়া থেকে মাটি সরিয়ে পোকা বের করে মেরে ফেলতে হবে। হালকা সেচ দিলে মাটির নীচে লুকিয়ে থাকা কীড়া মাটির উপরে আসবে, ফলে পাখি সহজে এদের ধরে খাবে বা হাত দ্বারা এদের মেরে ফেলা যাবে। এছাড়া বিষ ফাঁদ ব্যবহার করে এ পোকা দমন করা যায়। বিষ ফাঁদ তৈরী করার জন্য প্রতি হেক্টরে ২ কেজি সেভিন ৮৫ ডাব্লিউপি এর সাথে ১০০ কেজি গম বা ধানের কুড়া মিশ্রিত করে সন্ধ্যার সময় জমিতে প্রয়োগ করতে হবে। এছাড়া প্রতি লিটার পানির সাথে ৫ মিলি ডার্সবান ২০ ইসি বা পাইরিফস ২০ ইসি মিশিয়ে গাছের গোড়ার চারপাশে ভালভাবে স্প্রে করে পোকা দমন করা যায়।
